    private void tblpeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblpeminjamanMouseClicked
        // TODO add your handling code here:   
        datebalik.setEnabled(true);
        txtbayar.setEditable(true);
          try {
            int row=tblpeminjaman.getSelectedRow();
            String tblclick=(tblpeminjaman.getModel().getValueAt(row, 0).toString());
            String sql="select * from peminjaman where id_peminjaman='"+tblclick+"'";
            pst=koneksi.prepareStatement(sql);
            rst=pst.executeQuery();
            if (rst.next()) {
            String data1=rst.getString(("id_peminjaman"));
            txtid.setText(data1);
            String data2=rst.getString(("tanggal_pinjam"));
            txttglpeminjaman.setText(data2);
            String data3=rst.getString(("tanggal_kembali"));
            txttglkembali.setText(data3);
            bukukembali=rst.getString(("tanggal_kembali"));
            }
          }catch (Exception e) {JOptionPane.showMessageDialog(null, e);}
    }//GEN-LAST:event_tblpeminjamanMouseClicked

