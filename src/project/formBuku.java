    private void penerbit() throws SQLException {
        try {
            String query = "select * from penerbit";
            pst=koneksi.prepareStatement(query);
            java.sql.Statement st = koneksi.createStatement();
            java.sql.ResultSet rst = st.executeQuery(query);
        
            while(rst.next()){
            cbpenerbit.addItem(rst.getString("penerbit"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(formBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
