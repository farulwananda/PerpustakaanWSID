    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String tampilan ="yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggalbukukembali = String.valueOf(fm.format(datebalik.getDate()));
        //------------------------------------------------""-------------------------------------------
        String nis = txtid.getText();
        String admin = txtadmin.getText();
        String kembali = tanggalbukukembali;
        if(nis.equals("")||admin.equals("")||kembali.equals("")){
            JOptionPane.showMessageDialog(null,"Isi data dengan lengkap!");
        }else {
        try {
            String sql="insert into pengembalian (id_pengembalian,tanggal_pinjam,tanggal_kembali,dikembalikan, status,sanksi, id_petugas) value (?,?,?,?,?,?,?)";
            pst=koneksi.prepareStatement(sql);
            pst.setString(1, txtid.getText());
            pst.setString(2, txttglpeminjaman.getText());
            pst.setString(3, txttglkembali.getText());
            pst.setString(4, tanggalbukukembali);
            pst.setString(5, "Telah Dikembalikan");
            pst.setString(6, txtsanksi.getText());
            pst.setString(7, txtadmin.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
            }
        }
        
        try {
        String status = "Telah Dikembalikan";
            String sql ="UPDATE peminjaman SET status = '"+status
                    +"' WHERE id_peminjaman = '"+txtid.getText()+"'";
            pst=koneksi.prepareStatement(sql);
            pst.execute();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnsimpanActionPerformed
    
